    private void jButtonUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonUpdateActionPerformed
        ProdutoLoja updated = this.getSelected();

        if(updated == null){
            JOptionPane.showMessageDialog(this,
                "Nenhum produto em estoque foi selecionado",
                "Dados inconsistentes",
                JOptionPane.ERROR_MESSAGE);
            return;
        }
        try{
            this.ctrl.updateProdutoLoja(updated);
            JOptionPane.showMessageDialog(this,
                "Produto em estoque atualizado com sucesso",
                "Sucesso",
                JOptionPane.INFORMATION_MESSAGE);
        }catch(Exception e){
            JOptionPane.showMessageDialog(this,
                "Não foi possível atualizar este produto em estoque",
                "Erro ao atualizar.\nMensagem de erro: " + e.getMessage(),
                JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButtonUpdateActionPerformed

