    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        List<ProdutoLoja> searchResult = new ArrayList<>();
        String searchTerm = StringNormalizer.toNormalForm2(this.jtfSearchTerm.getText());
        for(int i = 0; i < this.jTable.getRowCount(); i++){
            ProdutoLoja p = this.tblModel.getObjectAt(i);
            String description = StringNormalizer.toNormalForm2(p.describe());
            System.out.println("PESQUISA: ["+ p.describe() +"] X ["+ searchTerm +"]");
            if (description.contains(searchTerm)) {
                System.out.println("CONTAINS!");
                searchResult.add(p);
            }
        }
        this.updateTable(searchResult);
    }//GEN-LAST:event_jButton2ActionPerformed

