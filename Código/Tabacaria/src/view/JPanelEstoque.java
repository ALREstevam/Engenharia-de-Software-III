     private void updateTable(List<ProdutoLoja> lst){
        String [] columns = new String[4];

        columns[0] = "id";
        columns[1] = "nome";
        columns[2] = "qtd_estoque";
        columns[3] = "preco_venda";

        this.tblModel = new GeneralTableModel<ProdutoLoja>(columns, lst);
        this.jTable.setModel(this.tblModel);
    }
     
      private void showSelected(){
        ProdutoLoja selected = this.getSelected();
        
        if(selected == null){
            System.err.println("SELECIONADO É NULL");
            return;
        }
        
        this.jListSelected.removeAll();
        
        List<ProdutoLoja> lstSelected = new ArrayList<>();
        lstSelected.add(selected);
        
        this.jListSelected.setModel(new GeneralComboboxModel<ProdutoLoja>().getComboBoxModelUsingDescription(lstSelected));
    }
    
    private ProdutoLoja getSelected(){
        int selectedIndex = this.jTable.getSelectedRow();
        if(selectedIndex < 0){
            return null;
        }
        return this.tblModel.getObjectAt(selectedIndex);
    }

