    private void jButtonRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonRegisterActionPerformed
        // TODO add your handling code here:
        
        if(this.jcnpjJftf.getText().isEmpty() || this.jcontatoJtf.getText().isEmpty() || this.jnameJtf.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Preencha todos os campos");
        }else{
            try{
                this.ctrl.addFornecedor(0, jnameJtf.getText(), jcontatoJtf.getText(), jcnpjJftf.getText());
            }
            catch(Exception e){
                JOptionPane.showMessageDialog(this,
                    "Algum campo não foi corretamente preenchido.\nMensagem de erro: " + e.getMessage() + "\n" + e.getCause(),
                    "Dados inconsistentes",
                    JOptionPane.ERROR_MESSAGE);
                return;
            }
            this.updateTable(this.ctrl.getFornecedores());
            JOptionPane.showMessageDialog(this,
                "Fornecedor cadastrado.",
                "Sucesso",
                JOptionPane.INFORMATION_MESSAGE);
        }
        
        
        
        
        
        
    }//GEN-LAST:event_jButtonRegisterActionPerformed

