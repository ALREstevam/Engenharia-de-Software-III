    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
        Fornecedor todelete = this.getSelected();

        if(todelete == null){
            JOptionPane.showMessageDialog(this,
                "Nenhum fornecedor foi selecionado",
                "Dados inconsistentes",
                JOptionPane.ERROR_MESSAGE);
            return;
        }

        try{
            this.ctrl.removeFornecedor(todelete);
            this.updateTable(this.ctrl.getFornecedores());
            JOptionPane.showMessageDialog(this,
                "Fornecedor deletado com sucesso.",
                "Sucesso",
                JOptionPane.INFORMATION_MESSAGE);
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(this,
                "Não foi possível remover o fornecedor.\nMensagem de erro: " + e.getMessage() + "\n" + e.getCause(),
                "Erro ao deletar",
                JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_deleteBtnActionPerformed

