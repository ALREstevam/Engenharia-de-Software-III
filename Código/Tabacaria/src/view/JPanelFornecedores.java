    private void updateTable(List<Fornecedor> lst){
        String [] columns = new String[4];

        columns[0] = "id";
        columns[1] = "nome";
        columns[2] = "contato";
        columns[3] = "cnpj";

        this.tblModel = new GeneralTableModel<Fornecedor>(columns, lst);
        this.jTable.setModel(this.tblModel);
    }
    
    private void showSelected(){
        Fornecedor selected = this.getSelected();
        
        if(selected == null){
            System.err.println("SELECIONADO É NULL");
            return;
        }
        
        this.jListSelected.removeAll();
        
        List<Fornecedor> lstSelected = new ArrayList<>();
        lstSelected.add(selected);
        
        this.jListSelected.setModel(new GeneralComboboxModel<Fornecedor>().getComboBoxModelUsingDescription(lstSelected));
    }
    
    private Fornecedor getSelected(){
        int selectedIndex = this.jTable.getSelectedRow();
        if(selectedIndex < 0){
            return null;
        }
        return this.tblModel.getObjectAt(selectedIndex);
    }
    

